public class PrimTypes {
    public static void main(String[] args) {
        boolean flag = true;
        char c = 'A';
        byte b = 100;
        short s = 32000;
        int i = 2_000_000;
        long l = 3_000_000_000L;
        float f = 32.85F;
        double d = 100.100;
        System.out.println("boolean: " + flag + "\nChar: " + c + "\nbyte: " + b +
                            "\nshort: " + s + "\nint: " + i + "\nlong: " + l +
                            "\nfloat: " + f + "\ndouble: " + d);
    }
}
