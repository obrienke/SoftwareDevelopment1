/*
 * Write a program which implements a while loop to output: 
 * "My Second While Loop" to the screen 5 times.
 */

 public class SecondLoop{
    public static void main(String[] args) {
        int x = 1;
        while(x <= 20){
            System.out.println("My Second While Loop");
            x++;
        }
    }
 }
 