public class PrimTypes {
    public static void main(String[] args) {
        boolean b = true;
        System.out.println("Boolean value is " + b);
        char c = 'A';
        System.out.println("char value is " + c); 
        byte num1 = 127;
        System.out.println("byte value is " + num1);
        short num2 = 129;
        System.out.println("short value is " + num2);
        int num3 = 13000;
        System.out.println("int value is " + num3);
        long num4 = 2_500_000_000l;
        System.out.println("long value is " + num4);
        float num5 = 34.23f;
        System.out.println("float value is " + num5);
        double num6 = 342342342.23213123;
        System.out.println("double value is " + num6);



    }
}
