public class ThreeMonths {
    public static void main(String[] args) {
        for(int i = 1; i <=3; i++){
            if(i == 1){
                System.out.println("\nJanuary");
            }else if( i == 2){
                System.out.println("February");
            }else{
                System.out.println("March\n");
            }
        }
    }
}
