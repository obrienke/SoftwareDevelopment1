public class Loops {
    public static void main(String[] args) {
        int i = 1;
        while(i <= 10){
            System.out.println(i + ". While Loop");
            i++;
        }
        System.out.println();
        for(i = 1; i <= 10; i++){
            System.out.println(i + ". For Loop");
        }
        System.out.println();
        for(int j = 1; j <= 3; j++){
            System.out.println(j + ". For Loop");
        }
        System.out.println();
        for(int j = 1; j <= 3; j++){
            System.out.println(j + ". For Loop");
        }
    }
}
